// Function to calculate the area of a rectangle
function calculateRectangleArea(width, height) {
  return width * height;
}

// Function to calculate the perimeter of a rectangle
function calculateRectanglePerimeter(width, height) {
  return 2 * (width + height);
}

// Main function to demonstrate Rectangle
console.log("Rectangle Area:", calculateRectangleArea(10, 5));
console.log("Rectangle Perimeter:", calculateRectanglePerimeter(10, 5));
